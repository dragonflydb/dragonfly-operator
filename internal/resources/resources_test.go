func newTestDragonfly(replicas int32) *resourcesv1.Dragonfly {
	return &resourcesv1.Dragonfly{
		TypeMeta: metav1.TypeMeta{
			APIVersion: "dragonflydb.io/v1alpha1",
			Kind:       "Dragonfly",
		},
		ObjectMeta: metav1.ObjectMeta{
			Name:      "test-df",
			Namespace: "default",
			UID:       types.UID("test-uid"),
		},
		Spec: resourcesv1.DragonflySpec{
			Replicas: replicas,
		},
	}
}

func TestGenerateDragonflyResources_ReadinessGateDisabled(t *testing.T) {
	df := newTestDragonfly(2)
	df.Spec.EnableReplicationReadinessGate = false

	resources, err := GenerateDragonflyResources(df, "", "")
	require.NoError(t, err)

	for _, obj := range resources {
		if sts, ok := obj.(*appsv1.StatefulSet); ok {
			assert.Empty(t, sts.Spec.Template.Spec.ReadinessGates,
				"readiness gates should be empty when feature is disabled")
			return
		}
	}
	t.Fatal("StatefulSet not found in generated resources")
}

func TestGenerateDragonflyResources_ReadinessGateEnabled(t *testing.T) {
	df := newTestDragonfly(2)
	df.Spec.EnableReplicationReadinessGate = true

	resources, err := GenerateDragonflyResources(df, "", "")
	require.NoError(t, err)

	for _, obj := range resources {
		if sts, ok := obj.(*appsv1.StatefulSet); ok {
			require.Len(t, sts.Spec.Template.Spec.ReadinessGates, 1,
				"should have exactly one readiness gate when feature is enabled")
			assert.Equal(t,
				corev1.PodConditionType(ReplicationReadyConditionType),
				sts.Spec.Template.Spec.ReadinessGates[0].ConditionType,
			)
			return
		}
	}
	t.Fatal("StatefulSet not found in generated resources")
}

func findNetworkPolicy(objs []client.Object) *networkingv1.NetworkPolicy {
	for _, obj := range objs {
		if np, ok := obj.(*networkingv1.NetworkPolicy); ok {
			return np
		}
	}
	return nil
}

func TestGenerateDragonflyResources_NetworkPolicyDefault(t *testing.T) {
	df := &resourcesv1.Dragonfly{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "test-df",
			Namespace: "default",
		},
		Spec: resourcesv1.DragonflySpec{
			Replicas: 1,
		},
	}

	objs, err := GenerateDragonflyResources(df, "", "dragonfly-system")
	require.NoError(t, err)

	np := findNetworkPolicy(objs)
	require.NotNil(t, np, "NetworkPolicy should be generated by default")

	assert.Equal(t, "test-df", np.Name)
	assert.Equal(t, "default", np.Namespace)

	assert.Equal(t, map[string]string{
		DragonflyNameLabelKey:     "test-df",
		KubernetesPartOfLabelKey:  KubernetesPartOf,
		KubernetesAppNameLabelKey: KubernetesAppName,
	}, np.Spec.PodSelector.MatchLabels)

	require.Len(t, np.Spec.Ingress, 2)
	assert.Equal(t, []networkingv1.PolicyType{networkingv1.PolicyTypeIngress}, np.Spec.PolicyTypes)

	protocolTCP := corev1.ProtocolTCP

	clientRule := np.Spec.Ingress[0]
	require.Len(t, clientRule.From, 1, "client port should be restricted to same namespace")
	assert.Equal(t, &metav1.LabelSelector{}, clientRule.From[0].PodSelector)
	assert.Nil(t, clientRule.From[0].NamespaceSelector, "client port should not allow cross-namespace")
	require.Len(t, clientRule.Ports, 1)
	assert.Equal(t, &protocolTCP, clientRule.Ports[0].Protocol)
	assert.Equal(t, intstr.FromInt32(DragonflyPort), *clientRule.Ports[0].Port)

	adminRule := np.Spec.Ingress[1]
	require.Len(t, adminRule.Ports, 1)
	assert.Equal(t, intstr.FromInt32(DragonflyAdminPort), *adminRule.Ports[0].Port)
	require.Len(t, adminRule.From, 2)

	operatorPeer := adminRule.From[0]
	assert.Equal(t, map[string]string{
		OperatorControlPlaneLabelKey: OperatorControlPlaneLabelValue,
	}, operatorPeer.PodSelector.MatchLabels)
	require.NotNil(t, operatorPeer.NamespaceSelector, "operator peer should allow cross-namespace")
	assert.Equal(t, map[string]string{
		KubernetesNamespaceLabelKey: "dragonfly-system",
	}, operatorPeer.NamespaceSelector.MatchLabels, "operator peer namespace should be pinned")

	peerPod := adminRule.From[1]
	assert.Equal(t, map[string]string{
		DragonflyNameLabelKey:     "test-df",
		KubernetesPartOfLabelKey:  KubernetesPartOf,
		KubernetesAppNameLabelKey: KubernetesAppName,
	}, peerPod.PodSelector.MatchLabels)
	assert.Nil(t, peerPod.NamespaceSelector, "peer pod should be same-namespace only")
}

